## =============================================================================
## HLA allele associations in cognitively healthy centenarians (CHC):
## comparison with literature effect sizes (UK Biobank, AD-GWAS, SARS-CoV-2)
##
## This script:
##   1. Loads regression results from the 100-plus Study (CHC vs. middle-aged
##      controls) and combines them with published effect sizes for the same
##      HLA alleles from three external sources:
##        - UK Biobank PheWAS (Butler-Laporte et al., 2024)
##        - Alzheimer's disease GWAS (Bellenguez et al., 2022)
##        - SARS-CoV-2 susceptibility (Augusto et al., 2021)
##   2. Computes a "Centenarian Effect Ratio" (CER) for each allele-trait pair:
##      the ratio of the (reversed-sign) CHC effect to the literature disease
##      effect, with bootstrapped confidence intervals and p-values.
##   3. Applies multiple testing correction (FDR).
##   4. Produces Figures 2-6 and Supplementary Figure 1, plus Supplementary
##      Tables 1-2 and Table 1.
##
## NOTE: Figure 1 is not generated by this script - it was created separately
## and is not based on the data processed here.
## =============================================================================

