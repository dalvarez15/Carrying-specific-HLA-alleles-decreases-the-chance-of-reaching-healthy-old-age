## -----------------------------------------------------------------------------
## Helper functions
## -----------------------------------------------------------------------------

# Computes the "Centenarian Effect Ratio" (CER) for a single allele-trait row.
# The CER is defined as: -beta_CHC / beta_literature
# (the minus sign converts the CHC effect, originally on the "becoming a CHC"
#  scale, to the same direction as the disease-risk effect in the literature).
#
# Confidence intervals and a two-tailed p-value are obtained by Monte Carlo
# simulation: both the CHC beta and the literature effect are sampled from
# normal distributions defined by their reported estimate and standard error,
# and the ratio of these draws is used to build an empirical distribution.
#
# Input: a single row (named vector / data.frame row) containing:
#   - beta, standard_error  : effect estimate and SE from the 100-plus Study
#   - effect, error.beta    : effect estimate and SE from the literature source
# Output: numeric vector c(ratioLCI, ratioUCI, one_tail_p, two_tail_p)
process_row_ctr_cent_new <- function(row) {
  numerator <- rnorm(n = 1e4, mean = -as.numeric(row["beta"]), sd = as.numeric(row["standard_error"])) # 100-plus
  denominator <- rnorm(n = 1e4, mean = as.numeric(row["effect"]), sd = as.numeric(row["error.beta"])) # Literature
  tmp_ratios <- numerator / denominator
  ratioLCI <- quantile(tmp_ratios, .025)[[1]]
  ratioUCI <- quantile(tmp_ratios, .975)[[1]]
  cdf <- ecdf(tmp_ratios)
  cdf2 <- cdf(0) # proportion of simulated ratios below zero
  if (cdf2 > 0.5) {
    one_tail_p <- 1 - cdf2
    two_tail_p <- one_tail_p * 2
  } else {
    one_tail_p <- cdf2
    two_tail_p <- one_tail_p * 2
  }
  # Return a numeric vector of the calculated values
  return(c(ratioLCI, ratioUCI, one_tail_p, two_tail_p))
}


## -----------------------------------------------------------------------------
## 1. Load input data
## -----------------------------------------------------------------------------

# HLA loci for which imputation is reliable (used to filter UKB associations
# and to label/order alleles by locus throughout the script)
imputable_loci <- c("A", "C", "B", "DRB1", "DQA1", "DQB1", "DPB1")

# Main regression results: HLA allele associations with CHC status (and
# sex-corrected versions), from the 100-plus Study
all_regression <- read.csv2("inputs/regression_npj_aging_2025.csv")
colnames(all_regression)[colnames(all_regression) == "odds_ratio"] <- "ors"
colnames(all_regression)[colnames(all_regression) == "freq_total"] <- "freq_all"

# UK Biobank PheWAS summary statistics (Butler-Laporte et al., 2024)
# Sheet 20 contains HLA allele-disease associations
ukbb_disease <- read_xlsx("inputs/BUTLER-LAPORTE_2024.xlsx", sheet = 20)

# Alzheimer's disease GWAS summary statistics (Bellenguez et al., 2022)
# Sheet 2 contains the relevant HLA allele associations
ad_lit <- read_excel("inputs/AD_GWAS_2022.xlsx", sheet = 2)
ad_lit <- separate(ad_lit, CI, into = c("l95", "u95"), sep = "-")  # split "lower-upper" 95% CI string
ad_lit$P <- as.numeric(gsub(" x 10", "e", ad_lit$P))               # convert "x 10^-n" notation to numeric
ad_lit$l95 <- as.numeric(ad_lit$l95)
ad_lit$effect <- log(ad_lit$OR)                                    # convert OR to log-odds (beta) scale
ad_lit$error.or <- (ad_lit$OR - ad_lit$l95) / 1.96                 # SE on the OR scale from the 95% CI
ad_lit$error.beta <- ad_lit$error.or / ad_lit$OR                   # approximate SE on the log-odds scale


## -----------------------------------------------------------------------------
## 2. Centenarians-controls vs. AD-GWAS literature effects
## -----------------------------------------------------------------------------

# Merge CHC association results (cent_ctr_original model) with AD-GWAS
# literature effects on matching alleles
cent_ctr_adlit <- merge(ad_lit, all_regression[which(all_regression$association == "cent_ctr_original"), ], by.x = "Allele", by.y = "allele")
cent_ctr_adlit$ratio <- -cent_ctr_adlit$beta / cent_ctr_adlit$effect

# Compute CER (ratio) confidence intervals and p-values per allele
new_columns <- t(sapply(split(cent_ctr_adlit, seq(nrow(cent_ctr_adlit))), process_row_ctr_cent_new))
colnames(new_columns) <- c("ratioLCI", "ratioUCI", "one_tail_p", "two_tail_p")
cent_ctr_adlit <- cbind(cent_ctr_adlit, new_columns)


## -----------------------------------------------------------------------------
## 3. UK Biobank associations: filtering and merging
## -----------------------------------------------------------------------------

# Select UKB associations meeting the significance threshold specified in the
# UKB paper: European ancestry, two-field allele resolution, Bonferroni-style
# threshold (5e-8 / 11 traits)
ukbb_tomerge <- ukbb_disease[which(ukbb_disease$Ancestry == "eur" & ukbb_disease$Pvalue < 0.00000005 / 11 &
                                     ukbb_disease$Type == "two_fields_allele"), ]

# Merge UKB-significant alleles with our CHC association results
ukbb_centctr <- merge(ukbb_tomerge, all_regression[which(all_regression$association == "cent_ctr_original"), ], by.x = "Marker", by.y = "allele")

# Filter out alleles with frequency < 1% (these would not reach the power
# threshold for significance and are excluded from downstream analysis)
ukbb_centctr <- subset(ukbb_centctr, freq_all > 0.01)


## -----------------------------------------------------------------------------
## 4. Compute Centenarian Effect Ratios for UKB-derived associations
## -----------------------------------------------------------------------------

# Standardise UKB literature effect column names to match process_row_ctr_cent_new()
colnames(ukbb_centctr)[colnames(ukbb_centctr) == "Beta"] <- "effect"
colnames(ukbb_centctr)[colnames(ukbb_centctr) == "SE"] <- "error.beta"
ukbb_centctr$ratio <- -ukbb_centctr$beta / ukbb_centctr$effect

# Compute CER confidence intervals and p-values per allele-trait pair
new_columns <- t(sapply(split(ukbb_centctr, seq(nrow(ukbb_centctr))), process_row_ctr_cent_new))
colnames(new_columns) <- c("ratioLCI", "ratioUCI", "one_tail_p", "two_tail_p")
ukbb_centctr <- cbind(ukbb_centctr, new_columns)

# Create a combined allele-trait identifier used for plotting and merging
ukbb_centctr <- ukbb_centctr %>%
  mutate(marker_trait = paste(Marker, Trait, sep = "_"))


## -----------------------------------------------------------------------------
## 5. Combine UKB-derived and AD-GWAS-derived associations into one data frame
## -----------------------------------------------------------------------------

# Select and harmonise columns from the UKB-based results
ukbb_centctr_ad <- ukbb_centctr %>%
  select(Marker, Trait, ratio, marker_trait, ratioLCI, ratioUCI, two_tail_p, effect, error.beta, beta, standard_error, p_value, freq_cases, freq_controls, freq_all, Allele_frequency, Pvalue, ors, lower_ci, upper_ci)

# Select and harmonise columns from the AD-GWAS-based results, renaming to
# match the UKB column names so the two data frames can be row-bound
ad_centad_toukb <- cent_ctr_adlit %>%
  select(Allele, ratio, ratioLCI, ratioUCI, two_tail_p, effect, error.beta, beta, standard_error, p_value, freq_cases, freq_controls, freq_all, Freq, P, ors, lower_ci, upper_ci)
colnames(ad_centad_toukb)[colnames(ad_centad_toukb) == "Allele"] <- "Marker"
colnames(ad_centad_toukb)[colnames(ad_centad_toukb) == "Freq"] <- "Allele_frequency"
colnames(ad_centad_toukb)[colnames(ad_centad_toukb) == "P"] <- "p_lit"
colnames(ukbb_centctr_ad)[colnames(ukbb_centctr_ad) == "Pvalue"] <- "p_lit"

# Tag AD-GWAS-derived rows with Trait = "AD" and build the matching identifier
ad_centad_toukb$Trait <- "AD"
ad_centad_toukb <- ad_centad_toukb %>%
  mutate(marker_trait = paste(Marker, Trait, sep = "_"))

ukbb_centctr_ad <- rbind(ukbb_centctr_ad, ad_centad_toukb)


## -----------------------------------------------------------------------------
## 6. Add SARS-CoV-2 association (Augusto et al., 2021)
## -----------------------------------------------------------------------------

# Manually define the published HLA-B*15:01 / SARS-CoV-2 association
# (asymptomatic vs. symptomatic infection, Augusto et al. 2021)
covid <- data.frame(
  Marker = "B*15:01",
  Trait = "Covid",
  marker_trait = "B*15:01_Covid",
  or = 2.38,
  orLCI = 1.51,
  orUCI = 3.65,
  p_lit = 3e-5,
  Freq_asymp = 0.1103,
  Freq_symp = 0.0495,
  Allele_frequency = NA,
  effect = NA,
  error.beta = NA
)
covid$error.beta <- (log(covid$orUCI) - log(covid$orLCI)) / (2 * 1.96) # SE on log-odds scale from 95% CI
covid$effect <- log(covid$or)                                          # convert OR to log-odds (beta)
covid$Allele_frequency <- covid$Freq_asymp + covid$Freq_symp           # combined allele frequency

# Merge the COVID literature effect with the corresponding CHC association
covid_cent_ctr <- merge(covid, all_regression[which(all_regression$association == "cent_ctr_original"), ], by.x = "Marker", by.y = "allele")

# Calculate CER, confidence intervals and p-value for B*15:01 / COVID
covid_cent_ctr$ratio <- -covid_cent_ctr$beta / covid_cent_ctr$effect
new_columns <- t(sapply(split(covid_cent_ctr, seq(nrow(covid_cent_ctr))), process_row_ctr_cent_new))
colnames(new_columns) <- c("ratioLCI", "ratioUCI", "one_tail_p", "two_tail_p")
covid_cent_ctr <- cbind(covid_cent_ctr, new_columns)

# Select and harmonise columns, then add to the combined data frame
covid_cent_ctr_torbind <- covid_cent_ctr %>%
  select(Marker, Trait, ratio, marker_trait, ratioLCI, ratioUCI, two_tail_p, effect, error.beta, beta, standard_error, p_value, freq_cases, freq_controls, freq_all, Allele_frequency, p_lit, ors, lower_ci, upper_ci)

ukbb_centctr_ad <- rbind(ukbb_centctr_ad, covid_cent_ctr_torbind)


## -----------------------------------------------------------------------------
## 7. Multiple testing correction
## -----------------------------------------------------------------------------

# FDR-correct the CHC association p-values (cent_ctr_original model),
# restricted to alleles with freq > 1% that are present in the combined
# allele-trait data frame
all_regression_corrected <- all_regression %>% subset(association == "cent_ctr_original" &
                                                                  freq_all > 0.01 &
                                                                  allele %in% ukbb_centctr_ad$Marker)
p_values <- all_regression_corrected$p_value
p_values_fdr <- p.adjust(p_values, method = "fdr")
length(p_values_fdr[which(p_values_fdr < 0.1)]) # check: 6 alleles significant at FDR < 0.1 (Table 1)
all_regression_corrected$p_corrected <- p_values_fdr

# Attach the FDR-corrected allele-level p-value to each allele-trait row
ukbb_centctr_ad <- merge(ukbb_centctr_ad, all_regression_corrected[, c("allele", "p_corrected")], by.x = "Marker", by.y = "allele", all.y = FALSE)

# FDR-correct the CER (ratio) two-tailed p-values across all allele-trait pairs
p_values <- ukbb_centctr_ad$two_tail_p
p_values_fdr <- p.adjust(p_values, method = "fdr")
length(p_values_fdr[which(p_values_fdr < 0.1)]) # check: 21 allele-disease associations significant at FDR < 0.1 (Figure 4)
ukbb_centctr_ad$two_tail_p_fdr <- p_values_fdr

