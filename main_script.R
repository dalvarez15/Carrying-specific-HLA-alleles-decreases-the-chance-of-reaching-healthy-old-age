## 11. Figure 4: corrplot of CER for FDR-significant alleles, by trait
## -----------------------------------------------------------------------------

# Identify alleles reaching FDR < 0.1 for the CER (allele-trait level), then
# keep all trait associations for those alleles
significant_marker_traits <- subset(ukbb_centctr_ad, two_tail_p_fdr < 0.1)
significant <- subset(ukbb_centctr_ad, Marker %in% significant_marker_traits$Marker)

# Set a fixed display order for the significant alleles
custom_order <- c("A*02:01", "C*03:04", "B*40:01", "DRB1*01:01", "DQA1*01:01", "DQB1*05:01")
significant$Marker <- factor(significant$Marker, levels = custom_order)

# Create the full grid of allele x trait combinations (so missing
# combinations appear as NA in the matrix rather than being dropped)
significant_combinations <- expand.grid(
  Marker = unique(significant$Marker),
  Trait = unique(significant$Trait)
)
significant_combinations <- significant_combinations %>%
  left_join(significant, by = c("Marker", "Trait")) %>%
  group_by(Marker, Trait)

# Build the CER (ratio) matrix: rows = traits, columns = alleles
ratio_matrix <- significant_combinations %>%
  summarise(ratio) %>%
  ungroup() %>%
  pivot_wider(names_from = Marker, values_from = ratio) %>%
  as.data.frame()
rownames(ratio_matrix) <- ratio_matrix$Trait
ratio_matrix$Trait <- NULL
